# remove() - returns error if item to be removed is absent.
# discard() - returns nothing or rather the same set even if there were no changes made. No errors.
# pop() - randomly pops/ deletes an item. We cannot specify index parameters.
# clear() -  clears everything to return an empty set.
# copy() - uses to create a copy since using assignment operator to create copies will not create real copy.



